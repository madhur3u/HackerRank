// https://www.hackerrank.com/challenges/maximum-draws/problem

import java.util.*;

public class Solution {
    public static void main(String[] args) {

        Scanner scn = new Scanner(System.in);
        int t = scn.nextInt();
        
        for (int t1 = 0; t1 < t; t1++){
            int n = scn.nextInt();
            System.out.println(n + 1);
        }
    }
}
